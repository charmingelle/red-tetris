export const pile = (state = initialState, action) => {
  switch (action.type) {
    case FILL_PILE: {
      const newState = JSON.parse(JSON.stringify(state));

      action.payload.coords.forEach(({ row, col }) => (newState[row][col] = 1));
      return newState;
    }
  }
  return state;
};
